package com.xworkz.chimney;

public class Chimney {

	private String brand;
	
	private double price;

	public Chimney(String brand, double price) {
		super();
		this.brand = brand;
		this.price = price;
	}

	public String getBrand() {
		return brand;
	}

	public double getPrice() {
		return price;
	}
	
	
}
